//Comparison Operators

//equal
'3' == 3; //true

//strictly equals
'3' === 3; //false

//Does not equal
'3' != '4';
console.log('3' != 3);

//Strictly no equal
'3' !== 3;

//greater than
3>2;

//less than
2<3;


//greater than or equal 2
3 >= 2;

//less than or equal 2
2 <=3;

//And 
2 && 3;

//Or
2 || 3;


